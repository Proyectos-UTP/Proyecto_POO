package POO_Proy_Paquete;
public class Sistema_ev_EsInf {
    private double pc1;
    private double aif1;
    private double pc2;
    private double pc3;
    private double pa;
    private double pc4;
    private double iF;

    public Sistema_ev_EsInf(double pc1, double aif1, double pc2, double pc3, double pa, double pc4, double iF) {
        this.pc1 = pc1;
        this.aif1 = aif1;
        this.pc2 = pc2;
        this.pc3 = pc3;
        this.pa = pa;
        this.pc4 = pc4;
        this.iF = iF;
    }

    public double getPc1() { return pc1; }
    public void setPc1(double pc1) { this.pc1 = pc1; }

    public double getAif1() { return aif1; }
    public void setAif1(double aif1) { this.aif1 = aif1; }

    public double getPc2() { return pc2; }
    public void setPc2(double pc2) { this.pc2 = pc2; }

    public double getPc3() { return pc3; }
    public void setPc3(double pc3) { this.pc3 = pc3; }

    public double getPa() { return pa; }
    public void setPa(double pc3) { this.pa = pa; }

    public double getPc4() { return pc4; }
    public void setPc4(double pc4) { this.pc4 = pc4; }

    public double getIF() { return iF; }
    public void setIF(double iF) { this.iF = iF; }
}
